# For Break The Loop -->

for x in range(1,11):

    if (x==5):
        break
    print("5*",x,"=",5*x)

print("Bahar Aagya loop Se!")

# For Continue The Loop -->

for x in range(1,11):

    if (x==5):
        continue
    print("5*",x,"=",5*x)

print("Bahar Aagya loop Se!")



